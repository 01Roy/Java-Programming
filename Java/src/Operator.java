public class Operator {
    public static void main(String[] args) {
        // increament and decrement operation
        int i = 45;
        System.out.println(i++); // this will print the value of i and then increment it from +1
        System.out.println(i);
        System.out.println(++i);// this will increment the value first and then print the increament value
    }

}
